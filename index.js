function add(a, b) {
  return parseInt(a) + parseInt(b);
}

function subtract( a, b ) {
  return parseInt(a) - parseInt(b);
}

function multiply(a, b) {
  return parseInt(a) * parseInt(b);
}

function divide(a, b) {
  return parseInt(a) / parseInt(b);
}

function inc(n) {
  return parseInt(++n);
}

function dec(n) {
  return parseInt(--n);
}

function makeInt(n) {
  parseInt(n, 10);
}